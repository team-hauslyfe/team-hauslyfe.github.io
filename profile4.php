                  <h4>Works</h4>
                  <ul class="list-group">
                     <li class="list-group-item">Born at the turn of the century, National Artist for Architecture Pablo Sebero Antonio pioneered modern Philippine architecture. His basic design is grounded on simplicity, no clutter. The lines are clean and smooth, and where there are curves, these are made integral to the structure. Pablo Jr. points out, “For our father, every line must have a meaning, a purpose. For him, function comes first before elegance or form“. The other thing that characterizes an Antonio structure is the maximum use of natural light and cross ventilation. Antonio believes that buildings “should be planned with austerity in mind and its stability forever as the aim of true architecture, that buildings must be progressive, simple in design but dignified, true to a purpose without resorting to an applied set of aesthetics and should eternally recreate truth”. <br>

Antonio’s major works include the following: Far Eastern University Administration and Science buildings; Manila Polo Club; Ideal Theater;Lyric Theater; Galaxy Theater; Capitan Luis Gonzaga Building; Boulevard-Alhambra (now Bel-Air) apartments; Ramon Roces Publications Building (now Guzman Institute of Electronics).</li>
                     
