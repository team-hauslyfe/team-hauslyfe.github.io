                  <h4>Biography</h4>
                  <p>
                    Pablo Sebero Antonio (January 25, 1901 – June 14, 1975)[1] was a Filipino architect. A pioneer of modern Philippine architecture,[2] he was recognized in some quarters as the foremost Filipino modernist architect of his time.[3] He was conferred the rank and title of National Artist of the Philippines by President Ferdinand Marcos in 1976.
                  </p>
                  <p>
                     Antonio was born in Binondo, Manila in 1901. He was orphaned by the age of 12, and had to work in the daytime in order to finish his high school education at night. He studied architecture at the Mapua Institute of Technology but dropped out of school.<br>

Ramon Arevalo, the engineer in charge of the Legislative Building project, funded Antonio's education at the University of London. He completed a five-year architecture course in three years, graduating in 1927.
                  </p>
