                  <p>
                     He was born Leandro on August 15, 1928, in Silay, Negros Occidental, a grandson of the first governor of the province. He later studied at the De La Salle Brothers in 1935 before returning to Negros due to the Second World War. He returned to Manila to study Pre-Law before shifting to pursue a Bachelor's Degree in Music at the University of Santo Tomas. Although he was a talented pianist, he later shifted again to Architecture, just a year before graduating. He married Cecilia Yulo, and one of their two children is also an architect.
