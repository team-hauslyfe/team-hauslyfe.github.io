                  <h4>Career and Works</h4>
                  <p>
                     Prior to serving as college dean, Professor Alfredo T. Juinio headed the UP Diliman Office of Campus Planning (also known as the Campus Planning Committee). Two of the projects he undertook during his term were the construction of C.P. Garcia Avenue and the UP Gateway at the University Avenue. <br>

Juinio, together with other UP engineering professors, became the structural engineers of the first thin-shell concrete dome in the Philippines for the UP Catholic Chapel, otherwise known as the Parish of the Holy Sacrifice.[1] The structure is the country's first circular church with the altar in the middle[1] and has been declared a historical landmark and cultural treasure by the National Historical Institute and the National Museum, respectively. <br>

Juinio was appointed as the Minister of Public Works and Highways, as well as the Administrator of the National Irrigation Administration. During his term, he headed the Upper Pampanga River Multipurpose Projects in Pantabangan, Nueva Ecija. <br>

From 1970 to 1979, Juinio served as the dean of the UP College of Engineering. During his term, he initiated the establishment of the National Engineering Center building, as well as the formation of the UP Engineering Research and Development Foundation, Inc. (UPERDFI). Juinio was elected as the first Chairman of the Board, and went on to serve as UPERDFI president for thirty years. <br>

Juinio was also the co-founder of the DCCD Engineering Corporation and a consultant to various government agencies.
