               <div class="bs-callout bs-callout-danger">
                  <h4>Life and Works</h4>
                  <ul class="list-group">
                     <li class="list-group-item">Juan M. Arellano was born on April 25, 1888 in Tondo Manila, Philippines to Luis C. Arellano and Bartola de Guzmán. Arellano was married Naty Ocampo on May 15, 1915. He had eight children, Oscar, Juanita, Cesar, Salvador, Juan Marcos, Luis, Gloria and Carlos.<br>

He attended the Ateneo Municipal de Manila and graduated in 1908. His first passion was painting and he trained under Lorenzo Guerrero, Toribio Antillon, and Fabian de la Rosa. However, he pursued architecture and was sent to the United States as one of the first pensionados in architecture, after Carlos Barreto, who was sent to the Drexel Institute in 1908; Antonio Toledo, who went to Ohio State; and Tomás Mapúa, who went to Cornell. <br>

Arellano went to the Pennsylvania Academy of the Fine Arts in 1911 and subsequently transferred to Drexel to finish his bachelor's degree in Architecture. He was trained in the Beaux Arts and subsequently went to work for George B. Post & Sons in New York City, where he worked for Frederick Law Olmsted, Jr. <br>

He then returned to the Philippines to begin a practice with his brother, Arcadio. He later joined the Bureau of Public Works just as the last American architects, George Fenhagen and Ralph H. Doane, were leaving. He and Tomás Mapúa were then named as supervising architects. In 1927, he took a study leave and went to the United States where he was greatly influenced by Art Deco architecture. <br>

In 1930, he returned to Manila and designed the Bulacan Provincial Capitol,Manila Metropolitan Theater, which was then considered controversially moderne.He continued to act as a consulting architect for the Bureau of Public Works where he oversaw the production of the Manila's first zoning plan. In 1940, he and Harry Frost created a design for Quezon City, which was to become the new capital of the Philippines. <br>

It was during that time that he designed the building that would house the United States High Commission to the Philippines, later the Embassy of the United States in Manila. He designed a demesne along the edge of Manila Bay, which featured a mission revival style mansion that took advantage of the seaside vista. The Americans instead opted for a federal-style building that ended up overpriced and uncomfortable. <br>

During World War II, the Legislative Building and Jones Bridge, were totally destroyed and the Post Office Building was severely damaged. While these structures were all reconstructed, his original designs were not followed and were considered poor replications.<br>

Arellano retired in 1956 and went back to painting. In 1960, he exhibited his work at the Manila YMCA.</li>
